#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	a;

	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n < 10)
	{
		n += 48;
		write(fd, &n, 1);
	}
	else
	{
		a = (n % 10) + 48;
		n /= 10;
		ft_putnbr_fd(n, fd);
		write(fd, &a, 1);
	}
}
